/* In JavaScript, the terms "postfix" and "prefix " are typically associated with increment and decrement operatore. ++ and --*/

// Prefix
let a = 5;
let b = ++a; // Increment a before using a value

console.log(a);
console.log(b);


// Postfix 

let a1 = 5;
let b1 = a1++; // Increment a after using its values

console.log(a1);
console.log(b1);


/* in the prefix form, the value of the variable is incremented or decremented before the value is used in the expression. In the postfix form, the value of the variable is used in the expression first, and then it is incremented or decremented.
*/